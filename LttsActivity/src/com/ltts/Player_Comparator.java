package com.ltts;

public class Player_Comparator {
	
	private String name ;
	private String skill ;
	
	public Player_Comparator(String name,String skill)
	{
		this.name=name;
		this.skill=skill;
		
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSkill() {
		return skill;
	}
	public void setSkill(String skill) {
		this.skill = skill;
	}
	
	
	


}
